function frutas(arr){
    let str = 'Sua cesta possui: '
    let obj = {}
    arr.forEach(e => {
        let vezesRepetidas = 0
        for(let c = 0; c < arr.length; c++){ 
            if(e == arr[c]){
                vezesRepetidas++
            }
        }
        obj[e] = vezesRepetidas
    })
    let indice = 0
    for(let keys in obj){
        indice++
        if(indice != Object.values(obj).length){
            str += `${obj[keys]} ${keys}, `
        }else{
            str += `${obj[keys]} ${keys}`
        }
    }
    console.log(str)
}

